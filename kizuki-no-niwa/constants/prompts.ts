export function getRandomPrompt(phase: number, day: number = 1): string {
    const prompts = DAILY_PROMPTS[phase] || DAILY_PROMPTS[1];
    // Use the day number as a seed for deterministic selection
    // (day - 1) % length ensures we cycle through prompts if there are more days than prompts
    const index = (day - 1) % prompts.length;
    return prompts[index];
}

/** Phase transition thresholds */
export const PHASE_THRESHOLDS: Record<number, number> = {
    2: 3,   // Day 3 -> Phase 2 (Accelerated for playability)
    3: 15,  // Day 15 -> Phase 3
    4: 30,  // Day 30 -> Phase 4
};

export function getNextPhase(currentDay: number): number {
    // Priority on thresholds (Day count)
    if (currentDay >= PHASE_THRESHOLDS[4]) return 4;
    if (currentDay >= PHASE_THRESHOLDS[3]) return 3;
    if (currentDay >= PHASE_THRESHOLDS[2]) return 2;
    return 1;
}
